//  Easy Vowel and consonet with switch 
#include<stdio.h>
int main()
{
     char ch;
     scanf("%c",&ch);
      
     switch(ch)//switch a---switch,case,break.default;
     {
          case 'a':
          case 'A':

          case 'u':

          case 'U':

          case 'e':
          
          case 'E':
       
          case 'i':
        
          case 'I':
         
          case 'o':
         
          case 'O':
         printf("Vowel\n");
          break;
          default:
          printf("Consonent\n");
     }
     return 0;
}
